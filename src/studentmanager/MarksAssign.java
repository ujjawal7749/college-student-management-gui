    private void getmarks()
    {
         try {
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            if (!checkmarksindb()) {
                ResultSet stud_branch = dbexec.retv("select branch_id from student where id=" + ad(jTextField1.getText()));
                stud_branch.next();
                String branch_id = (stud_branch.getString(1));
                ResultSet semsubjects = dbexec.retv("select subject_id,name from semsubjects  where branch_id=" + ad(branch_id) + " and sem=" + ad(jTextField2.getText()));
                while (semsubjects.next()) {
                    model.addRow(new Object[]{semsubjects.getString(1), semsubjects.getString(2), ""});
                }
            }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
    
    public boolean checkmarksindb() {
        try {
            ResultSet marksrecord = dbexec.retv("select marks from studentmarks where student_id=" + ad(jTextField1.getText()) + " and sem=" + ad(jTextField2.getText()));
            if (!marksrecord.next()) {
                return false;
            } else {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);
                Hashtable<String, String> ht = Commons.slicer(marksrecord.getString(1));
                for (String k : ht.keySet()) {
                    model.addRow(new Object[]{k, "", ht.get(k)});
                }
                return true;
            }
        } catch (Exception e) {
            System.out.println(e.toString());
            return false;
        }
        //while(stud_branch.next()) branch_id = (stud_branch.getString(1));
    }

