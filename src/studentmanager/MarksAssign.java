    public String stringform() {
        String st = "";
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            st = st + jTable1.getValueAt(i, 0) + "," + jTable1.getValueAt(i, 2) + ";";
        }
        return st;
    }

